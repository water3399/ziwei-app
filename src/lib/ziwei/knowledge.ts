// 根據雙星組合查詢解釋
export function getComboInterpretation(star1: string, star2: string): string | null {
  return DOUBLE_STAR_COMBOS[`${star1}+${star2}`]
    || DOUBLE_STAR_COMBOS[`${star2}+${star1}`]
    || null;
}

// 根據宮位名稱查詢含義
export function getPalaceMeaning(palaceName: string) {
  // 處理 iztro 輸出的簡稱（如「兄弟」vs「兄弟宮」）
  const normalized = palaceName.replace('宮', '');
  return PALACE_MEANINGS[palaceName] || PALACE_MEANINGS[normalized] || null;
}
